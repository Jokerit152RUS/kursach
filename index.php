        var SavedSearchesApplication = (function () {
            var mainSavedSearchesContainer = document.getElementsByClassName('saved-searches-desktop-home')[0];
            var showAllButton = mainSavedSearchesContainer.getElementsByClassName('all-searches-button')[0];
            var searchesCountElement = mainSavedSearchesContainer.getElementsByClassName('searches-count-element')[0];

            function itemsCount() {
                return mainSavedSearchesContainer.getElementsByClassName('saved-search').length;
            }

            function hideShowAllButton() {
                if (showAllButton) {
                    showAllButton.style.display = 'none';
                }
            }

            function showNewItem() {
                var s = mainSavedSearchesContainer.getElementsByClassName('saved-search');
                for (var i = 0; i < s.length; i++) {
                    if (s[i].classList.contains('hide')) {
                        s[i].classList.remove('hide');
                        break;
                    }
                }
            }

            function removeItem(element) {
                element.parentNode.removeChild(element);
            }

            function decreaseSearchesCount() {
                if (searchesCountElement) {
                    searchesCountElement.innerHTML = itemsCount();
                }
            }

            function getId(element) {
                return element.getAttribute('data-id');
            }

            function isSubscribedFunc(element) {
                return element.getAttribute('data-subscribed') == 'true';
            }

            var searches = mainSavedSearchesContainer.getElementsByClassName('delete-button');
            for (var i = 0; i < searches.length; i++) {
                searches[i].addEventListener('click', function (e) {
                    var element = e.target.parentNode.parentNode;
                    var id = getId(element);
                    var isSubscribed = isSubscribedFunc(element);

                    var removeFunction = function () {
                        removeItem(element);
                        decreaseSearchesCount();

                        if (itemsCount() <= 5) {
                            hideShowAllButton();
                        }

                        showNewItem();

                        SKELBIU.API.SavedSearchAPI.deleteSearch(id, function (response) {
                            if (!response.success) {
                                alert(response.message);
                            }
                        });
                    };

                    if (isSubscribed) {
                        ConfirmDialog.setSuccessCallback(removeFunction);
                        ConfirmDialog.confirm();
                    } else {
                        removeFunction();
                    }

                    e.preventDefault();
                });
            }
        })();
